    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        // Get the input values
    String buyerIdStr = txtID.getText().trim();
    String fullName = txtName.getText().trim();
    String contactNoStr = txtContact.getText().trim();
    String gender = (String) jComboBoxGender.getSelectedItem();
    String email = txtemail.getText().trim();
    String address = txtaddress.getText().trim();
    String paymentStr = txtPayment.getText().trim();
    String duePaymentStr = txtduepayment.getText().trim();

    // Validate input fields using BuyerValidator
    if (!BuyerValidator.validateFields(buyerIdStr, fullName, contactNoStr, email, address, gender, paymentStr, duePaymentStr)) {
        return; // Exit the method if validation fails
    }

    // If validation passes, proceed to add the buyer
    try {
        int buyerId = Integer.parseInt(buyerIdStr);
        long contactNo = Long.parseLong(contactNoStr);
        double payment = Double.parseDouble(paymentStr);
        double duePayment = Double.parseDouble(duePaymentStr);

        // Check if the Buyer ID already exists
        for (BuyerModel buyer : buyerList) {
            if (buyer.getBuyerId() == buyerId) {
                JOptionPane.showMessageDialog(this, "Buyer ID already exists. Please use a unique Buyer ID.", "Input Error", JOptionPane.ERROR_MESSAGE);
                return; // Exit the method if Buyer ID is not unique
            }
        }

        // Create a new BuyerModel object
        BuyerModel newBuyer = new BuyerModel(buyerId, fullName, contactNo, gender, email, address, payment, duePayment);

        // Add the new buyer to the list
        buyerList.add(newBuyer);

        // Sort the buyer list by ID (or by name if you prefer)
        SelectionSort selectionSort = new SelectionSort();
        List<BuyerModel> sortedList = selectionSort.sortByBuyerId(buyerList, false); // Sort in ascending order

        // Update the table model with the sorted buyer data
        updateTableModel(sortedList); // Pass the sorted list to the method

        // Show a success message
        JOptionPane.showMessageDialog(this, "Buyer added successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);

        // Clear the input fields
        resetFields();
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "An error occurred while processing the input. Please check your values.", "Input Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnAddActionPerformed

private void searchBuyerByName(String searchValue) {
    // Sort the buyer list by name before searching
    SelectionSort selectionSort = new SelectionSort();
    List<BuyerModel> sortedList = selectionSort.sortByFullName(buyerList, false); // Sort in ascending order

    // Perform the binary search
    BinarySearch binarySearch = new BinarySearch();
    BuyerModel foundBuyer = binarySearch.searchByName(searchValue, sortedList, 0, sortedList.size() - 1);

    // Check if the buyer was found and show a message
    if (foundBuyer != null) {
        String message = "Buyer Found:\n"
                + "ID: " + foundBuyer.getBuyerId() + "\n"
                + "Name: " + foundBuyer.getFullName() + "\n"
                + "Contact No: " + foundBuyer.getContactNo() + "\n"
                + "Email: " + foundBuyer.getEmail() + "\n"
                + "Address: " + foundBuyer.getAddress() + "\n"
                + "Gender: " + foundBuyer.getGender() + "\n"
                + "Payment: " + foundBuyer.getPayment() + "\n"
                + "Due Payment: " + foundBuyer.getDuePayment();

        // Show a message dialog with the found buyer's details
        JOptionPane.showMessageDialog(this, message, "Buyer Details", JOptionPane.INFORMATION_MESSAGE);
    } else {
        JOptionPane.showMessageDialog(this, "Buyer not found.", "Search Result", JOptionPane.INFORMATION_MESSAGE);
    }

    // Clear the search fields after the search
    clearSearchFields();
}
