    private void btnGenerateReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerateReportActionPerformed
        // TODO add your handling code here:                                                                                                 
    // Create a StringBuilder to hold the report content
    StringBuilder reportContent = new StringBuilder();
    
    // Add a title to the report
    reportContent.append("Report of Buyers\n");
    reportContent.append("=====================\n\n");
    
    // Add details of each buyer to the report
    for (BuyerModel buyer : buyerList) {
        reportContent.append("Buyer ID: ").append(buyer.getBuyerId()).append("\n");
        reportContent.append("Full Name: ").append(buyer.getFullName()).append("\n");
        reportContent.append("Contact No: ").append(buyer.getContactNo()).append("\n");
        reportContent.append("Email: ").append(buyer.getEmail()).append("\n");
        reportContent.append("Address: ").append(buyer.getAddress()).append("\n");
        reportContent.append("Gender: ").append(buyer.getGender()).append("\n");
        reportContent.append("Payment: ").append(buyer.getPayment()).append("\n");
        reportContent.append("Due Payment: ").append(buyer.getDuePayment()).append("\n");
        reportContent.append("---------------------\n");
    }

    // Create a JTextArea to display the report
    JTextArea textArea = new JTextArea(reportContent.toString());
    textArea.setEditable(false); // Make it read-only
    textArea.setLineWrap(true); // Enable line wrapping
    textArea.setWrapStyleWord(true); // Wrap at word boundaries

    // Create a JScrollPane to allow scrolling
    JScrollPane scrollPane = new JScrollPane(textArea);
    scrollPane.setPreferredSize(new java.awt.Dimension(400, 300)); // Set preferred size for the scroll pane

    // Show the report in a pop-up dialog
    JOptionPane.showMessageDialog(this, scrollPane, "Generated Report", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnGenerateReportActionPerformed
