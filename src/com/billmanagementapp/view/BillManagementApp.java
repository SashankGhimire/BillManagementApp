    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:                                                                               
    String buyerIdStr = txtidupdate.getText().trim();
    String fullName = txtName1.getText().trim();
    String contactNoStr = txtContact1.getText().trim();
    String gender = (String) jComboBox2.getSelectedItem();
    String email = txtemail1.getText().trim();
    String address = txtaddress1.getText().trim();
    String paymentStr = txtProduct1.getText().trim();
    String duePaymentStr = txtproductprice1.getText().trim();

    // Validate input fields using BuyerValidator
    if (!BuyerValidator.validateFields(buyerIdStr, fullName, contactNoStr, email, address, gender, paymentStr, duePaymentStr)) {
        return; // Exit the method if validation fails
    }

    try {
        int buyerId = Integer.parseInt(buyerIdStr);
        long contactNo = Long.parseLong(contactNoStr);
        double payment = Double.parseDouble(paymentStr);
        double duePayment = Double.parseDouble(duePaymentStr);

        // Find the buyer in the list and update their details
        for (BuyerModel buyer : buyerList) {
            if (buyer.getBuyerId() == buyerId) {
                buyer.setFullName(fullName);
                buyer.setContactNo(contactNo);
                buyer.setGender(gender);
                buyer.setEmail(email);
                buyer.setAddress(address);
                buyer.setPayment(payment);
                buyer.setDuePayment(duePayment);

                // Update the table model
                updateTableModel(buyerList);

                // Show a success message
                JOptionPane.showMessageDialog(this, "Buyer details updated successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
                
                // Reset the fields after updating
                resetUpdateFields();
                return; // Exit after updating
            }
        }

        // If buyer is not found
        JOptionPane.showMessageDialog(this, "Buyer not found.", "Update Error", JOptionPane.ERROR_MESSAGE);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "An error occurred while processing the input. Please check your values.", "Input Error", JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_btnUpdateActionPerformed

