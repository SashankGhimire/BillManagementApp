private void updateTableModel(List<BuyerModel> buyers) {
    // Clear the existing rows in the table models
    buyerTableModel.setRowCount(0);
    DefaultTableModel buyerDetailsTableModel = (DefaultTableModel) tblAdmin1.getModel();
    buyerDetailsTableModel.setRowCount(0); // Clear the second table as well

    // Add each buyer from the provided list to both table models
    for (BuyerModel buyer : buyers) {
        buyerTableModel.addRow(new Object[]{
            buyer.getBuyerId(),
            buyer.getFullName(),
            buyer.getContactNo(),
            buyer.getAddress(),
            buyer.getGender(),
            buyer.getEmail(),
            buyer.getPayment(),
            buyer.getDuePayment()
        });

        // Add to the second table
        buyerDetailsTableModel.addRow(new Object[]{
            buyer.getBuyerId(),
            buyer.getFullName(),
            buyer.getContactNo(),
            buyer.getAddress(),
            buyer.getGender(),
            buyer.getEmail(),
            buyer.getPayment(),
            buyer.getDuePayment()
        });
    }
}

private void sortBuyersById(boolean isDesc) {
    SelectionSort selectionSort = new SelectionSort();
    List<BuyerModel> sortedList = selectionSort.sortByBuyerId(buyerList, isDesc);
    updateTableModel(sortedList); // Pass the sorted list
}

