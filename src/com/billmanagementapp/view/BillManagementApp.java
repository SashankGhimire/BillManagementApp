    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:                                                                                  
    String buyerIdStr = txtidupdate1.getText().trim();

    // Validate Buyer ID input
    if (buyerIdStr.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter a Buyer ID to delete.", "Input Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    try {
        int buyerId = Integer.parseInt(buyerIdStr);
        BuyerModel buyerToRemove = null;

        // Search for the buyer in the list
        for (BuyerModel buyer : buyerList) {
            if (buyer.getBuyerId() == buyerId) {
                buyerToRemove = buyer;
                break;
            }
        }

        // If buyer is found, confirm deletion
        if (buyerToRemove != null) {
            int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this buyer?", "Confirm Deletion", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                buyerList.remove(buyerToRemove); // Remove the buyer from the list
                updateTableModel(buyerList); // Update the table model
                JOptionPane.showMessageDialog(this, "Buyer deleted successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
                
                // Reset the fields after deletion
                resetDeleteFields();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Buyer not found.", "Deletion Error", JOptionPane.ERROR_MESSAGE);
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Buyer ID must be a numeric value.", "Input Error", JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_btnRemoveActionPerformed

